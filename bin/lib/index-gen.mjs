// CONTENT_INDEX project-layer generator (ADR-021). Projects per-file frontmatter
// (`description`, `key_facts`, `questions`, `noindex`) into a marker-delimited
// section of CONTENT_INDEX.md. The framework seed and any hand-written entries
// outside the markers are never touched (ADR-016); regeneration replaces only
// what sits between the markers, so the output cannot rot relative to sources.
import { readFileSync, readdirSync, existsSync } from 'fs'
import { join } from 'path'
import { parseFrontmatter } from './md-rules.mjs'

// Keep aligned with DIRS in lib/scripts/check-index.sh — the generator indexes
// exactly what registration enforces (an invariants test binds the two lists).
export const PROJECT_DIRS = ['wiki', 'findings', 'plans', 'conclusions', 'deliverables']

export const MARKER_BEGIN = '<!-- canon:project-layer:begin — generated by `canon index` from file frontmatter; entries between markers are overwritten on regeneration -->'
export const MARKER_END = '<!-- canon:project-layer:end -->'

// wiki/client + wiki/user are frontmatter-banned (ADR-012 — served whole by
// MCP), so their entries fall back to the file's H1 title as a one-liner.
const FRONTMATTER_BANNED = ['wiki/client/', 'wiki/user/']

function walkMdFiles(root, rel = '', out = []) {
  const abs = join(root, rel)
  if (!existsSync(abs)) return out
  for (const entry of readdirSync(abs, { withFileTypes: true })) {
    const relPath = rel ? `${rel}/${entry.name}` : entry.name
    if (entry.isDirectory()) {
      if (entry.name === '_archive') continue
      walkMdFiles(root, relPath, out)
    } else if (entry.name.endsWith('.md') && entry.name !== 'README.md') {
      out.push(relPath)
    }
  }
  return out
}

const isNonEmptyString = (v) => typeof v === 'string' && v.trim() !== ''
const isNonEmptyList = (v) => Array.isArray(v) && v.length > 0

function firstHeading(content) {
  const m = content.match(/^#+\s+(.+)$/m)
  return m ? m[1].trim() : null
}

// Render one entry, or null when the file opts out / has nothing to project.
// Returns { entry, warning } — warning set when usable fields were dropped.
export function renderEntry(relPath, content) {
  const filename = relPath.split('/').pop()
  const link = `### [${filename}](./${relPath})`

  if (FRONTMATTER_BANNED.some((p) => relPath.startsWith(p))) {
    const title = firstHeading(content) ?? filename
    return { entry: `${link}\n${title} — served whole via MCP (no frontmatter by ADR-012).`, warning: null }
  }

  const fm = parseFrontmatter(content)
  if (!fm || fm.noindex === true) return { entry: null, warning: null }
  if (!isNonEmptyString(fm.description)) return { entry: null, warning: null }

  const full = isNonEmptyList(fm.key_facts) && isNonEmptyList(fm.questions)
  if (full) {
    return {
      entry: [
        link,
        `**What it is:** ${fm.description}`,
        '',
        '**Key facts:**',
        ...fm.key_facts.map((f) => `- ${f}`),
        '',
        '**Questions it answers:**',
        ...fm.questions.map((q) => `- ${q}`),
      ].join('\n'),
      warning: null,
    }
  }
  const partial = isNonEmptyList(fm.key_facts) || isNonEmptyList(fm.questions)
  return {
    entry: `${link}\n${fm.description}`,
    warning: partial
      ? `${relPath}: rendered lightweight — full blocks need all of description, key_facts, questions (two-form contract)`
      : null,
  }
}

// Build the full project-layer section for a consumer root.
export function renderProjectLayer(consumerRoot) {
  const entries = []
  const warnings = []
  for (const dir of PROJECT_DIRS) {
    const files = walkMdFiles(consumerRoot, dir).sort()
    const dirEntries = []
    for (const relPath of files) {
      const { entry, warning } = renderEntry(relPath, readFileSync(join(consumerRoot, relPath), 'utf8'))
      if (warning) warnings.push(warning)
      if (entry) dirEntries.push(entry)
    }
    if (dirEntries.length > 0) {
      entries.push(`## ${dir}/\n\n${dirEntries.join('\n\n---\n\n')}`)
    }
  }
  const body = entries.length > 0
    ? entries.join('\n\n')
    : '_No indexed project files yet — add `description:` frontmatter to files in the project dirs and re-run `canon index`._'
  return {
    section: `${MARKER_BEGIN}\n\n${body}\n\n${MARKER_END}`,
    warnings,
    count: entries.reduce((n, group) => n + group.split('\n### ').length - 1, 0),
  }
}

// Splice the generated section into the index: replace between markers when
// present, append the marked section otherwise (legacy index migration path —
// hand-written entries stay untouched outside the markers).
export function applyProjectLayer(indexContent, section) {
  const begin = indexContent.indexOf(MARKER_BEGIN)
  const end = indexContent.indexOf(MARKER_END)
  if (begin !== -1 && end !== -1 && end > begin) {
    return indexContent.slice(0, begin) + section + indexContent.slice(end + MARKER_END.length)
  }
  return `${indexContent.replace(/\n*$/, '\n')}\n${section}\n`
}
